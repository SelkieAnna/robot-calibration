        Ry(q(4)+error(12))*Tx(error(13))*Tz(error(14))*Rz(error(15))* ...
        Rx(q(5)+error(16))*Tz(error(17))*Rz(error(18))* ...
        Ry(q(6))*Rx(error(19))*Rz(error(20));
end

